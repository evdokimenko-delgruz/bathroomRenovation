// Функция для парсинга статей
const parseArticles = (text) => {
    const articleBlocks = text.split('Статья ').filter(Boolean).map(block => `Статья ${block}`);
    return articleBlocks.map((block, index) => {
        const lines = block.split('\n').filter(Boolean);
        const titleLine = lines[0].trim();
        const titleMatch = titleLine.match(/Статья \d+: "([^"]+)"/);
        const title = titleMatch ? titleMatch[1] : `Статья ${index + 1}`;
        const content = lines.slice(1).join('\n').trim();

        return {
            id: index + 1,
            title: title,
            content: content
        };
    });
};

const articles = parseArticles(articlesRawText);

// Функция для парсинга советов
const parseTips = (text) => {
    const tipBlocks = text.split('\n\n').filter(Boolean);
    return tipBlocks.map((block, index) => {
        const lines = block.split('\n').filter(Boolean);
        const title = lines[0].replace(/^\d+\.\s/, '').trim();
        const content = lines.slice(1).join('\n').trim();
        return {
            id: index + 1,
            title: title,
            content: content
        };
    });
};

const tips = parseTips(tipsRawText);

