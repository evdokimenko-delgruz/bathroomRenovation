const parseTestimonials = (text) => {
    return text.split('\n\nОтзыв ').filter(Boolean).map((item, index) => {
        const lines = item.split('\n').filter(Boolean);
        const authorLine = lines[0].includes(':') ? lines[0].split(':')[1].trim() : lines[0].trim(); // Извлекаем имя автора
        const textContent = lines.slice(1).join('\n').trim().replace(/"/g, ''); // Извлекаем текст отзыва, удаляем кавычки
        return {
            id: index + 1,
            author: authorLine,
            text: textContent
        };
    });
};

